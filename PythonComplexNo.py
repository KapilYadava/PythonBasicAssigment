# Write program to Add, Subtract, Multiply, Divide 2 Complex numbers.

a = complex(10, 20)
b = complex(20, 45)

print('Adding {} and {} = {}'.format(a, b, a + b))
print('Subtracting {} and {} = {}'.format(a, b, a - b))
print('Multiplying {} and {} = {}'.format(a, b, a * b))
print('Dividing {} and {} = {}'.format(a, b, a / b))
